function norm(value) {
  if (typeof value !== 'string') throw new Error('planned path must be a string');
  if (value.includes('\\') || value.includes('\0') || value.startsWith('/') || /^[a-zA-Z]:/.test(value) || value.split('/').some(part => !part || part === '.' || part === '..')) {
    throw new Error(`unsafe planned path: ${value}`);
  }
  return value;
}

function outputPath(targetRoot, plan, relPath) {
  return resolveManagedPath(targetRoot, plan.outputRoot, norm(relPath));
}

function fileEntry(manifest, action, outputRoot) {
  return manifest?.entries.find(entry => entry.adapter === action.adapter && entry.outputRoot === outputRoot && entry.relPath === action.relPath && entry.kind === 'file');
}

function isManagedByAction(action, clearRoots) {
  return clearRoots.some(root => root.adapter === action.adapter && isUnderRoot(action.relPath, root.relPath));
}

function classifyDestination(targetRoot, plan, action, manifest, stale, clearRoots) {
  let path;
  try { path = outputPath(targetRoot, plan, action.relPath); } catch (error) {
    return { action, relPath: action.relPath, adapter: action.adapter, status: 'unsafe-path', message: error.message, forceOverride: false };
  }
  if (action.type === 'json-merge' || action.type === 'gitignore-append') {
    if (existsSync(path) && action.type === 'json-merge') {
      try {
        const parsed = JSON.parse(readFileSync(path, 'utf8'));
        if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) throw new Error('top level is not an object');
      } catch (error) {
        return { action, relPath: action.relPath, adapter: action.adapter, status: 'malformed-shared', message: `Shared config is malformed: ${error.message}`, forceOverride: false };
      }
    }
    return { action, relPath: action.relPath, adapter: action.adapter, status: existsSync(path) ? 'mergeable-shared' : 'absent', message: 'Shared config will be merged semantically', forceOverride: false };
  }
  if (!existsSync(path)) return { action, relPath: action.relPath, adapter: action.adapter, status: 'absent', message: 'Path does not exist; safe to create', forceOverride: false };
  const owned = fileEntry(manifest, action, plan.outputRoot);
  if (!owned) return { action, relPath: action.relPath, adapter: action.adapter, status: 'unowned', message: 'File exists but is not owned by Agentic Loop; refusing to overwrite', forceOverride: false };
  return hashFile(path) === owned.hash
    ? { action, relPath: action.relPath, adapter: action.adapter, status: 'exact-owned', message: 'Previously generated by Agentic Loop; safe to refresh', forceOverride: false }
    : { action, relPath: action.relPath, adapter: action.adapter, status: 'owned-modified', message: 'Previously generated by Agentic Loop but has been modified; refusing to overwrite', forceOverride: true };
}

function classifyFileDestination(targetRoot, plan, action, manifest, stale, clearRoots) {
  let path;
  try { path = outputPath(targetRoot, plan, action.relPath); } catch (error) {
    return { action, relPath: action.relPath, adapter: action.adapter, status: 'unsafe-path', message: error.message, forceOverride: false };
  }
  if (!existsSync(path)) return { action, relPath: action.relPath, adapter: action.adapter, status: 'absent', message: 'Path does not exist; safe to create', forceOverride: false };

  const owned = fileEntry(manifest, action, plan.outputRoot);
  if (owned) {
    return hashFile(path) === owned.hash
      ? { action, relPath: action.relPath, adapter: action.adapter, status: 'exact-owned', message: 'Previously generated by Agentic Loop; safe to refresh', forceOverride: false }
      : { action, relPath: action.relPath, adapter: action.adapter, status: 'owned-modified', message: 'Previously generated by Agentic Loop but has been modified; refusing to overwrite', forceOverride: true };
  }

  const owners = (manifest?.entries ?? []).filter(entry =>
    entry.kind === 'file' && entry.outputRoot === plan.outputRoot && entry.relPath === action.relPath
  );
  if (!owners.length) {
    return { action, relPath: action.relPath, adapter: action.adapter, status: 'unowned', message: 'File exists but is not owned by Agentic Loop; refusing to overwrite', forceOverride: false };
  }
  if (!isManagedByAction(action, clearRoots)) {
    return { action, relPath: action.relPath, adapter: action.adapter, status: 'outside-managed-root', message: 'Ownership transfer destination is outside the adapter managed surface', forceOverride: false };
  }
  const staleIds = new Set(stale.map(entryIdentity));
  if (owners.some(owner => !staleIds.has(entryIdentity(owner)))) {
    return { action, relPath: action.relPath, adapter: action.adapter, status: 'active-owner', message: 'File has active ownership from another adapter; refusing to overwrite', forceOverride: false };
  }
  if (owners.some(owner => hashFile(path) !== owner.hash)) {
    const displacedAdapters = [...new Set(owners.map(owner => owner.adapter))].join(', ');
    return {
      action,
      relPath: action.relPath,
      adapter: action.adapter,
      status: 'transfer-owned-modified',
      message: `Ownership transfer from ${displacedAdapters} to ${action.adapter} is unsafe because the stale generated file was modified; refusing to overwrite`,
      forceOverride: false,
    };
  }
  return { action, relPath: action.relPath, adapter: action.adapter, status: 'exact-owned-transfer', message: 'Exact stale ownership will transfer in this transaction', forceOverride: false };
}

function staleFileEntries(manifest, outputRoot, actions) {
  const clearRoots = actions
    .filter(action => action.type === 'clear-owned-directory')
    .map(action => ({ adapter: action.adapter, relPath: norm(action.relPath) }));
  const plannedFiles = new Set(actions
    .filter(action => action.type === 'write-file')
    .map(action => entryKey(action.adapter, outputRoot, action.relPath, 'file')));
  return {
    clearRoots,
    stale: (manifest?.entries ?? []).filter(entry => entry.kind === 'file' && entry.outputRoot === outputRoot &&
      clearRoots.some(root => root.adapter === entry.adapter && isUnderRoot(entry.relPath, root.relPath)) &&
      !plannedFiles.has(entryKey(entry.adapter, outputRoot, entry.relPath, 'file'))),
  };
}

export function preflightPlan(targetRoot, plan, forceGenerated = false) {
  let outputRoot;
  let manifest = null;
  try { outputRoot = normalizeOutputRoot(plan.outputRoot ?? '.'); manifest = loadManifest(targetRoot); } catch (error) {
    return { collisions: [{ relPath: '(manifest)', adapter: '(none)', status: 'unsupported-manifest', message: error.message, forceOverride: false }], blocked: [{ relPath: '(manifest)', adapter: '(none)', status: 'unsupported-manifest', message: error.message, forceOverride: false }], allClear: false };
  }
  plan.outputRoot = outputRoot;
  const collisions = [];
  const destinations = new Map();
  const { clearRoots, stale } = staleFileEntries(manifest, outputRoot, plan.actions);
  for (const action of plan.actions) {
    if (!['write-file', 'json-merge', 'gitignore-append'].includes(action.type)) continue;
    const key = action.relPath;
    const prior = destinations.get(key);
    if (prior && (prior.type !== action.type || prior.content !== action.content)) {
      collisions.push({ relPath: action.relPath, adapter: action.adapter, status: 'duplicate-destination', message: `Conflicts with ${prior.adapter} output`, forceOverride: false });
      continue;
    }
    destinations.set(key, action);
    collisions.push(action.type === 'write-file'
      ? classifyFileDestination(targetRoot, plan, action, manifest, stale, clearRoots)
      : classifyDestination(targetRoot, plan, action, manifest));
  }
  const blocked = collisions.filter(c =>
    !['absent', 'exact-owned', 'exact-owned-transfer', 'mergeable-shared'].includes(c.status) &&
    !(forceGenerated && c.forceOverride === true)
  );
  return { collisions, blocked, allClear: blocked.length === 0 };
}

export function formatCollisions(collisions) {
  return collisions.filter(c => c.status !== 'absent').map(c => `${['exact-owned', 'exact-owned-transfer', 'mergeable-shared'].includes(c.status) ? '  ' : '  BLOCKED '}${c.relPath}: ${c.message}`);
}

function pointerParts(pointer) {
  if (typeof pointer !== 'string' || !pointer.startsWith('/')) throw new Error(`invalid JSON pointer: ${pointer}`);
  return pointer.slice(1).split('/').map(part => part.replace(/~1/g, '/').replace(/~0/g, '~'));
}

function equalJson(left, right) { return JSON.stringify(left) === JSON.stringify(right); }

/**
 * Find the exact prior replace-array-element mutation for the given identity:
 * adapter + outputRoot + relPath + matchKey + matchValue.
 * Returns the mutation object or null.
 */
export function replaceMutationIdentity({ adapter, outputRoot, relPath, pointer, matchKey, matchValue }) {
  return [adapter, outputRoot, relPath, pointer, matchKey, JSON.stringify(matchValue)].join('\u0000');
}

function findPriorReplaceMutation(priorManifest, outputRoot, relPath, adapter, pointer, matchKey, matchValue) {
  if (!priorManifest) return null;
  const identity = replaceMutationIdentity({ adapter, outputRoot, relPath, pointer, matchKey, matchValue });
  for (const entry of priorManifest.entries) {
    if (entry.kind !== 'shared-config' || !Array.isArray(entry.mutations)) continue;
    for (const mutation of entry.mutations) {
      if (mutation.op !== 'replace-array-element') continue;
      if (replaceMutationIdentity({ adapter: entry.adapter, outputRoot: entry.outputRoot, relPath: entry.relPath, pointer: mutation.pointer, matchKey: mutation.matchKey, matchValue: mutation.matchValue }) === identity) return mutation;
    }
  }
  return null;
}

function applyJsonMerge(path, action, priorManifest, outputRoot, existingDoc) {
  const existed = existingDoc ? true : existsSync(path);
  const original = existingDoc ?? (existed ? readFileSync(path, 'utf8') : '');
  const lineEnding = original.includes('\r\n') ? '\r\n' : '\n';
  let value = existed ? JSON.parse(original) : {};
  if (!value || typeof value !== 'object' || Array.isArray(value)) throw new Error(`${action.relPath} must contain a JSON object`);
  const applied = [];
  let changed = false;
  for (const mutation of action.mutations) {
    const pointer = mutation.pointer.startsWith('/') ? mutation.pointer : `/${mutation.pointer}`;
    const parts = pointerParts(pointer);
    let container = value;
    const createdContainers = [];
    for (let index = 0; index < parts.length - 1; index++) {
      const part = parts[index];
      if (container[part] === undefined) { container[part] = {}; createdContainers.push('/' + parts.slice(0, index + 1).join('/')); changed = true; }
      if (!container[part] || typeof container[part] !== 'object' || Array.isArray(container[part])) throw new Error(`${action.relPath}: ${mutation.pointer} has a non-object parent`);
      container = container[part];
    }
    const key = parts.at(-1);
    if (mutation.op === 'array-add') {
      if (container[key] === undefined) { container[key] = []; createdContainers.push(pointer); changed = true; }
      if (!Array.isArray(container[key])) throw new Error(`${action.relPath}: ${mutation.pointer} is not an array`);
      // Look up prior mutation for exact identity (adapter + pointer + value) to preserve createdContainers.
      const priorMutation = priorManifest?.entries.find(entry =>
        entry.kind === 'shared-config' && entry.outputRoot === outputRoot && entry.relPath === action.relPath &&
        entry.adapter === action.adapter &&
        entry.mutations?.some(m => m.op === 'array-add' && m.pointer === pointer && equalJson(m.value, mutation.value))
      )?.mutations?.find(m => m.op === 'array-add' && m.pointer === pointer && equalJson(m.value, mutation.value));
      const mergedContainers = [...new Set([...(priorMutation?.createdContainers ?? []), ...createdContainers])];
      if (!container[key].some(item => equalJson(item, mutation.value))) {
        container[key].push(mutation.value);
        changed = true;
        applied.push({ op: mutation.op, pointer, value: mutation.value, added: true, createdContainers: mergedContainers });
      } else {
        applied.push({ op: mutation.op, pointer, value: mutation.value, added: Boolean(priorMutation?.added), createdContainers: mergedContainers });
      }
    } else if (mutation.op === 'set-if-absent') {
      if (container[key] === undefined) {
        container[key] = mutation.value;
        changed = true;
        applied.push({ op: mutation.op, pointer, value: mutation.value, added: true, createdContainers });
      } else {
        const priorMutation = priorManifest?.entries.find(entry =>
          entry.kind === 'shared-config' && entry.outputRoot === outputRoot && entry.relPath === action.relPath &&
          entry.adapter === action.adapter &&
          entry.mutations?.some(m => m.op === 'set-if-absent' && m.pointer === pointer && equalJson(m.value, mutation.value))
        )?.mutations?.find(m => m.op === 'set-if-absent' && m.pointer === pointer && equalJson(m.value, mutation.value));
        applied.push({ op: mutation.op, pointer, value: mutation.value, added: Boolean(priorMutation?.added), createdContainers: [...new Set([...(priorMutation?.createdContainers ?? []), ...createdContainers])] });
      }
    } else if (mutation.op === 'replace-array-element') {
      // When targeting an undefined property, initialize it as an empty array
      // and record the container as created (Defect 8).
      if (container[key] === undefined) {
        container[key] = [];
        createdContainers.push(pointer);
        changed = true;
      }
      if (!Array.isArray(container[key])) throw new Error(`${action.relPath}: ${mutation.pointer} is not an array`);
      const index = container[key].findIndex(item => item && typeof item === 'object' && item[mutation.matchKey] === mutation.matchValue);
      if (index >= 0) {
        const current = container[key][index];
        if (equalJson(current, mutation.value)) {
          // Already at desired value. Look up the exact prior mutation
          // record for this adapter+pointer+matchKey identity.
          const priorMutation = findPriorReplaceMutation(priorManifest, outputRoot, action.relPath, action.adapter, pointer, mutation.matchKey, mutation.matchValue);
          const mergedContainers = [...new Set([...(priorMutation?.createdContainers ?? []), ...createdContainers])];
          if (priorMutation) {
            // Prior record exists. Preserve its original lineage:
            // - If it was originally added by AL (added: true), keep added: true
            // - If it had a previous field, keep that exact previous
            // - If it omitted previous (AL originally appended), continue omitting it
            const hasOwnPrevious = Object.hasOwn(priorMutation, 'previous');
            if (hasOwnPrevious) {
              applied.push({ op: mutation.op, pointer, value: mutation.value, previous: priorMutation.previous, added: Boolean(priorMutation.added), createdContainers: mergedContainers, matchKey: mutation.matchKey, matchValue: mutation.matchValue });
            } else {
              applied.push({ op: mutation.op, pointer, value: mutation.value, added: Boolean(priorMutation.added), createdContainers: mergedContainers, matchKey: mutation.matchKey, matchValue: mutation.matchValue });
            }
          } else {
            // No prior record. The matching element exists but was not
            // created by Agentic Loop. Record as user-owned (added: false).
            applied.push({ op: mutation.op, pointer, value: mutation.value, added: false, createdContainers: mergedContainers, matchKey: mutation.matchKey, matchValue: mutation.matchValue });
          }
        } else {
          // Current value differs from desired. Only replace when an exact
          // prior mutation proves ownership with matching adapter + pointer.
          const priorMutation = findPriorReplaceMutation(priorManifest, outputRoot, action.relPath, action.adapter, pointer, mutation.matchKey, mutation.matchValue);
          if (!priorMutation) throw new Error(`${action.relPath}: ${pointer}: refusing to replace a user-owned ${mutation.matchKey} entry`);
          // The prior record must have been added (we generated the current value).
          if (priorMutation.added !== true || !equalJson(current, priorMutation.value)) {
            throw new Error(`${action.relPath}: ${pointer}: refusing to replace a modified or user-owned ${mutation.matchKey} entry`);
          }
          container[key][index] = mutation.value;
          changed = true;
          const mergedContainers = [...new Set([...(priorMutation.createdContainers ?? []), ...createdContainers])];
          const hasOwnPrevious = Object.hasOwn(priorMutation, 'previous');
          if (hasOwnPrevious) {
            applied.push({ op: mutation.op, pointer, value: mutation.value, previous: priorMutation.previous, added: true, createdContainers: mergedContainers, matchKey: mutation.matchKey, matchValue: mutation.matchValue });
          } else {
            applied.push({ op: mutation.op, pointer, value: mutation.value, added: true, createdContainers: mergedContainers, matchKey: mutation.matchKey, matchValue: mutation.matchValue });
          }
        }
      } else {
        container[key].push(mutation.value);
        changed = true;
        applied.push({ op: mutation.op, pointer, value: mutation.value, added: true, createdContainers, matchKey: mutation.matchKey, matchValue: mutation.matchValue });
      }
    } else throw new Error(`unsupported JSON mutation: ${mutation.op}`);
  }
  return { content: JSON.stringify(value, null, 2).replace(/\n/g, lineEnding) + lineEnding, createdFile: !existed, mutations: applied, changed };
}

function appendGitignore(path, action, priorManifest, outputRoot) {
  const existed = existsSync(path);
  const original = existed ? readFileSync(path, 'utf8') : '';
  const lineEnding = original.includes('\r\n') ? '\r\n' : '\n';
  const line = action.line.trim();
  const lines = original.split(/\r?\n/);
  const matchCount = lines.filter(value => value.trim() === line).length;

  // Find prior ownership record for this exact adapter + line identity.
  const priorEntry = priorManifest?.entries.find(
    entry => entry.kind === 'gitignore-line' && entry.outputRoot === outputRoot &&
             entry.relPath === action.relPath && entry.line === line &&
             entry.adapter === action.adapter
  );

  if (matchCount > 0) {
    // Line already exists on disk.
    if (priorEntry) {
      // We have a prior ownership record. Carry it forward.
      // If duplicates make identity ambiguous (matchCount > 1 and we can't
      // tell which is ours), preserve all lines and retain the entry but
      // mark it as ambiguous so removal won't guess.
      if (priorEntry.ambiguous || matchCount > 1) {
        // Ambiguous duplicates: more than one matching line means we
        // cannot safely identify which one is ours. Preserve all lines
        // and keep the entry.
        return { content: original, lineAdded: false, createdFile: priorEntry.createdFile, occurrence: priorEntry.occurrence, ambiguous: true };
      }
      return { content: original, lineAdded: false, createdFile: priorEntry.createdFile, occurrence: priorEntry.occurrence };
    }
    // No prior ownership record. The line existed before Agentic Loop.
    // Do NOT claim ownership. Return a signal that prevents creating a
    // manifest entry (Defect 1).
    return { content: original, lineAdded: false, createdFile: false, occurrence: 0, unowned: true };
  }

  // Line does not exist. Add it.
  const separator = original && !original.endsWith('\n') ? lineEnding : '';
  return { content: `${original}${separator}${line}${lineEnding}`, lineAdded: true, createdFile: !existed, occurrence: matchCount, lineEnding };
}

function atomicWrite(path, content) {
  mkdirSync(dirname(path), { recursive: true });
  const temporary = `${path}.${randomUUID()}.tmp`;
  try { writeFileSync(temporary, content); renameSync(temporary, path); } finally { if (existsSync(temporary)) rmSync(temporary, { force: true }); }
}

function snapshot(paths) {
  return [...paths].map(path => ({ path, existed: existsSync(path), bytes: existsSync(path) ? readFileSync(path) : null }));
}
function removeEmptyDirectory(path) {
  if (!existsSync(path) || !statSync(path).isDirectory() || readdirSync(path).length !== 0) return false;
  rmdirSync(path);
  return true;
}
function rollback(snapshots) {
  const errors = [];
  for (const item of [...snapshots].reverse()) try {
    if (item.existed) atomicWrite(item.path, item.bytes);
    else if (existsSync(item.path)) rmSync(item.path, { force: true });
  } catch (error) { errors.push(`${item.path}: ${error.message}`); }
  return errors;
}
function emptyParents(target, paths) {
  for (const path of paths) {
    let current = dirname(path);
    while (current !== target && current.startsWith(target + sep) && removeEmptyDirectory(current)) current = dirname(current);
  }
}

function missingAncestorDirectories(targetRoot, paths) {
  const missing = new Set();
  for (const path of paths) {
    let current = dirname(path);
    while (current !== targetRoot && current.startsWith(targetRoot + sep)) {
      if (!existsSync(current)) missing.add(current);
      current = dirname(current);
    }
  }
  return [...missing].sort((left, right) => right.length - left.length);
}

function entryKey(adapter, outputRoot, relPath, kind) {
  return [adapter, outputRoot, relPath, kind].join('\u0000');
}

function isUnderRoot(relPath, root) {
  return relPath === root || relPath.startsWith(`${root}/`);
}

export function executeGenerationPlan(targetRoot, plan, options = {}) {
  const preflight = preflightPlan(targetRoot, plan, Boolean(options.forceGenerated));
  if (!preflight.allClear) return { ok: false, errors: preflight.blocked.map(item => `BLOCKED ${item.relPath}: ${item.message}`), writtenFiles: [], adapters: plan.adapters };
  const previous = loadManifest(targetRoot);
  const current = previous ?? createManifest(options.packageVersion ?? loadPackageVersion());
  const outputRoot = plan.outputRoot;
  const ordinary = plan.actions.filter(action => action.type === 'write-file');
  const merges = plan.actions.filter(action => action.type === 'json-merge');
  const ignores = plan.actions.filter(action => action.type === 'gitignore-append');
  const { clearRoots, stale } = staleFileEntries(current, outputRoot, plan.actions);
  const extraWrites = options.extraWrites ?? [];
  for (const extra of extraWrites) resolveManagedPath(targetRoot, '.', norm(extra.relPath));

  // Group merges by destination so multiple mutations to the same file are
  // applied sequentially to one evolving in-memory document (Defect 7).
  const mergesByDest = new Map();
  for (const action of merges) {
    const dest = action.relPath;
    if (!mergesByDest.has(dest)) mergesByDest.set(dest, []);
    mergesByDest.get(dest).push(action);
  }

  const removableStale = stale.filter(entry => {
    const path = resolveManagedPath(targetRoot, outputRoot, entry.relPath);
    return !existsSync(path) || hashFile(path) === entry.hash;
  });
  const staleWarnings = stale.filter(entry => !removableStale.includes(entry)).map(entry => `Preserved modified stale generated file: ${entry.relPath}`);

  // Track which destinations existed before writing.
  const existedBefore = new Map();
  for (const action of ordinary) {
    const p = outputPath(targetRoot, plan, action.relPath);
    existedBefore.set(p, existsSync(p));
  }

  // Compute merge and ignore results inside the transaction boundary.
  // For each destination, read the file once and apply every action to the
  // evolving in-memory document so the second action sees the first action's
  // result (Defect 9).
  let computedMerges = [];
  let computedIgnores = [];
  try {
    for (const [dest, actions] of mergesByDest) {
      const path = outputPath(targetRoot, plan, dest);
      let currentDoc = null;
      for (const action of actions) {
        const result = applyJsonMerge(path, action, previous, outputRoot, currentDoc);
        computedMerges.push({ action, result });
        // Pass the serialized result as the starting document for the next action.
        currentDoc = result.content;
      }
    }
    computedIgnores = ignores.map(action => ({ action, result: appendGitignore(outputPath(targetRoot, plan, action.relPath), action, previous, outputRoot) }));
  } catch (error) {
    return { ok: false, errors: [error.message], writtenFiles: [], adapters: plan.adapters };
  }

  const paths = new Set([
    ...ordinary.map(action => outputPath(targetRoot, plan, action.relPath)),
    ...computedMerges.map(({ action }) => outputPath(targetRoot, plan, action.relPath)),
    ...computedIgnores.map(({ action }) => outputPath(targetRoot, plan, action.relPath)),
    ...extraWrites.map(extra => resolveManagedPath(targetRoot, '.', extra.relPath)),
    ...removableStale.map(entry => resolveManagedPath(targetRoot, outputRoot, entry.relPath)),
  ]);
  const manifestPath = resolveManagedPath(targetRoot, '.', '.agenticloop/generated-artifacts.json');
  const manifestSnapshot = existsSync(manifestPath) ? readFileSync(manifestPath) : null;
  const snapshots = snapshot(paths);
  const createdDirs = missingAncestorDirectories(resolve(targetRoot), [...paths, manifestPath]);

  // Reconcile only exact identities refreshed by this plan. Entries outside
  // declared adapter surfaces remain owned, including modified stale files.
  const refreshed = new Set([
    ...ordinary.map(action => entryKey(action.adapter, outputRoot, action.relPath, 'file')),
    ...computedMerges.map(({ action }) => entryKey(action.adapter, outputRoot, action.relPath, 'shared-config')),
    ...computedIgnores.map(({ action }) => entryKey(action.adapter, outputRoot, action.relPath, 'gitignore-line')),
  ]);
  const removedStale = new Set(removableStale.map(entry => entryIdentity(entry)));
  const retained = current.entries.filter(entry => !refreshed.has(entryKey(entry.adapter, entry.outputRoot, entry.relPath, entry.kind)) && !removedStale.has(entryIdentity(entry)));
  const next = { ...current, schemaVersion: 3, packageVersion: loadPackageVersion(), entries: retained };
  for (const action of ordinary) {
    const p = outputPath(targetRoot, plan, action.relPath);
    next.entries.push(createFileEntry({ adapter: action.adapter, outputRoot, relPath: action.relPath, content: action.content, marker: action.marker, existence: existedBefore.get(p) ? 'refreshed' : 'created' }));
  }
  for (const { action, result } of computedMerges) {
    if (!result.mutations.length) continue;
    const priorEntry = current.entries.find(e => e.kind === 'shared-config' && e.outputRoot === outputRoot && e.relPath === action.relPath && e.adapter === action.adapter);
    const createdFile = priorEntry?.createdFile ?? result.createdFile;
    const existingIdx = next.entries.findIndex(e => e.kind === 'shared-config' && e.outputRoot === outputRoot && e.relPath === action.relPath && e.adapter === action.adapter);
    const entry = createSharedConfigEntry({ adapter: action.adapter, outputRoot, relPath: action.relPath, createdFile: existingIdx >= 0 ? next.entries[existingIdx].createdFile ?? createdFile : createdFile, mutations: existingIdx >= 0 ? [...next.entries[existingIdx].mutations, ...result.mutations] : result.mutations });
    if (existingIdx >= 0) next.entries[existingIdx] = entry; else next.entries.push(entry);
  }
  for (const { action, result } of computedIgnores) {
    if (result.unowned) continue;
    const priorEntry = current.entries.find(e => e.kind === 'gitignore-line' && e.outputRoot === outputRoot && e.relPath === action.relPath && e.line === action.line.trim() && e.adapter === action.adapter);
    next.entries.push(createGitignoreEntry({ adapter: action.adapter, outputRoot, relPath: action.relPath, line: action.line, occurrence: result.occurrence, createdFile: result.createdFile, ambiguous: Boolean(result.ambiguous || priorEntry?.ambiguous) }));
  }
  try { validateManifest(next); } catch (error) {
    return { ok: false, errors: [error.message], writtenFiles: [], adapters: plan.adapters };
  }

  try {
    for (const entry of removableStale) {
      const path = resolveManagedPath(targetRoot, outputRoot, entry.relPath);
      if (existsSync(path)) rmSync(path);
    }
    for (const action of ordinary) atomicWrite(outputPath(targetRoot, plan, action.relPath), action.content);
    for (const { action, result } of computedMerges) if (result.changed) atomicWrite(outputPath(targetRoot, plan, action.relPath), result.content);
    for (const { action, result } of computedIgnores) if (result.lineAdded) atomicWrite(outputPath(targetRoot, plan, action.relPath), result.content);
    for (const extra of extraWrites) atomicWrite(resolveManagedPath(targetRoot, '.', extra.relPath), extra.content);
    saveManifest(targetRoot, next);
    emptyParents(resolve(targetRoot), removableStale.map(entry => resolveManagedPath(targetRoot, outputRoot, entry.relPath)));
    return { ok: true, errors: staleWarnings, writtenFiles: [...paths].map(path => relative(resolveManagedPath(targetRoot, outputRoot, '.'), path).replaceAll('\\', '/')), adapters: plan.adapters };
  } catch (error) {
    const rollbackErrors = rollback(snapshots);
    // Roll back transaction-created empty directories (Defect 10).
    // Walk each created dir tree deepest-first and remove empty ancestors.
    for (const dir of createdDirs) try { removeEmptyDirectory(dir); } catch (dirError) { rollbackErrors.push(`dir rollback ${dir}: ${dirError.message}`); }
    try { if (manifestSnapshot) atomicWrite(manifestPath, manifestSnapshot); else if (existsSync(manifestPath)) rmSync(manifestPath, { force: true }); } catch (manifestError) { rollbackErrors.push(`manifest: ${manifestError.message}`); }
    return { ok: false, errors: [error.message, ...rollbackErrors], writtenFiles: [], adapters: plan.adapters };
  }
}

export function resolveOutputDir(target, outputDirOpt) {
  const targetRoot = resolve(target);
  if (!outputDirOpt) return targetRoot;
  const candidate = resolve(targetRoot, outputDirOpt);
  const root = relative(targetRoot, candidate).replaceAll('\\', '/') || '.';
  resolveManagedPath(targetRoot, root, '.');
  return candidate;
}
export function computeOutputRoot(target, outputDir) {
  const root = relative(resolve(target), resolve(outputDir)).replaceAll('\\', '/') || '.';
  return normalizeOutputRoot(root);
}
