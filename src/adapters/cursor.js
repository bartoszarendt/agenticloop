/**
 * Cursor host adapter.
 *
 * Status: experimental.
 *
 * Repo-local Cursor MVP support focuses on explicit activation plus one public
 * Agentic Loop skill surface:
 *   - Custom subagents under .cursor/agents/*.md.
 *   - One public .cursor/skills/agenticloop/SKILL.md skill.
 *   - Internal procedure references under
 *     .cursor/skills/agenticloop/references/.
 *
 * This MVP intentionally does NOT generate .cursor/rules/ or any always-on
 * hooks. Agentic Loop stays explicit-activation only: invoke /agenticloop in
 * Cursor Agent chat when you want the workflow.
 */

