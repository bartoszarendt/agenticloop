export const CURSOR_GENERATED_MARKER = 'Generated by: agenticloop generate cursor';
export const CURSOR_SKILL_RELATIVE_PATH =
  `.cursor/skills/${CURSOR_PUBLIC_SKILL_NAME}/SKILL.md`;
