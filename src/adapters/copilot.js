export function resolveCopilotAgentPath(outputDir, agentName) {
  return join(outputDir, '.github', 'agents', `${agentName}.agent.md`);
}

export function resolveCopilotPromptPath(outputDir) {
  return join(outputDir, COPILOT_PROMPT_RELATIVE_PATH);
}

export function resolveCopilotPublicSkillPath(outputDir) {
  return join(outputDir, COPILOT_SKILL_RELATIVE_PATH);
}

function generatedTextIncludesMarker(text) {
  return typeof text === 'string' && text.includes(COPILOT_GENERATED_MARKER);
}

function readGeneratedTextIfExists(filePath) {
  if (!existsSync(filePath)) return '';
  try {
    return readFileSync(filePath, 'utf-8');
  } catch {
    return '';
  }
}

export function listGeneratedCopilotAgentFiles(rootDir) {
  const agentsDir = join(rootDir, '.github', 'agents');
  if (!existsSync(agentsDir) || !statSync(agentsDir).isDirectory()) return [];

  const files = [];
  for (const entry of readdirSync(agentsDir)) {
    if (!entry.endsWith('.agent.md')) continue;
    const fullPath = join(agentsDir, entry);
    if (!statSync(fullPath).isFile()) continue;
    if (generatedTextIncludesMarker(readGeneratedTextIfExists(fullPath))) {
      files.push(fullPath);
    }
  }

  return files.sort();
}

export function isGeneratedCopilotSkill(rootDir) {
  return generatedTextIncludesMarker(readGeneratedTextIfExists(resolveCopilotPublicSkillPath(rootDir)));
}

export function isGeneratedCopilotPrompt(rootDir) {
  return generatedTextIncludesMarker(readGeneratedTextIfExists(resolveCopilotPromptPath(rootDir)));
}

export function generatedCopilotArtifactsPresent(rootDir) {
  const present = [];
  if (listGeneratedCopilotAgentFiles(rootDir).length > 0) {
    present.push('.github/agents/');
  }
  if (isGeneratedCopilotSkill(rootDir)) {
    present.push(COPILOT_SKILL_RELATIVE_PATH);
  }
  if (isGeneratedCopilotPrompt(rootDir)) {
    present.push(COPILOT_PROMPT_RELATIVE_PATH);
  }
  return present;
}

function clearGeneratedCopilotAgents(outputDir) {
  for (const filePath of listGeneratedCopilotAgentFiles(outputDir)) {
    rmSync(filePath, { force: true });
  }
}

function writeCopilotPublicSkill(outputDir, skillEntries, skillReferenceMap, agentNames, backendEntries) {
  const skillDir = join(outputDir, '.github', 'skills', COPILOT_PUBLIC_SKILL_NAME);
  if (existsSync(skillDir)) {
    rmSync(skillDir, { recursive: true, force: true });
  }
  mkdirSync(skillDir, { recursive: true });

  const files = [];
  const skillPath = join(skillDir, 'SKILL.md');
  writeFileSync(skillPath, renderCopilotPublicSkill(skillReferenceMap, agentNames, backendEntries), 'utf-8');
  files.push(relative(outputDir, skillPath).replace(/\\/g, '/'));

  files.push(...writeCopilotSkillReferences(
    skillEntries,
    skillReferenceMap,
    join(skillDir, 'references', 'skills'),
    outputDir
  ));

  files.push(...writeCopilotBackendReferences(
    backendEntries,
    join(skillDir, 'references', 'backends'),
    outputDir,
    skillReferenceMap
  ));

  return files;
}

export function generateCopilotArtifacts(alConfig, repoRoot, outputDir) {
  const copilotAdapter = alConfig.adapters?.copilot ?? {};
  const roles = alConfig.roles ?? {};
  const roleBindings = copilotAdapter.roleBindings ?? {};
  const agentNames = Object.fromEntries(
    Object.keys(roles).map(roleName => [roleName, roleBindings[roleName]?.agent ?? roleName])
  );
  const skillEntries = readCanonicalSkillEntries(repoRoot, alConfig);
  const backendEntries = readCanonicalBackendEntries(repoRoot, alConfig);
  const relativeSkillReferenceMap = buildSkillReferenceMap(
    skillEntries,
    copilotSkillReferenceRelativePath
  );
  addBackendReferencesToMap(relativeSkillReferenceMap, backendEntries, copilotBackendReferenceRelativePath);
  const absoluteSkillReferenceMap = buildSkillReferenceMap(
    skillEntries,
    copilotSkillReferenceAbsolutePath
  );
  addBackendReferencesToMap(absoluteSkillReferenceMap, backendEntries, copilotBackendReferenceAbsolutePath);

  mkdirSync(outputDir, { recursive: true });
  clearGeneratedCopilotAgents(outputDir);

  const files = [];
  const agentsDir = join(outputDir, '.github', 'agents');
  mkdirSync(agentsDir, { recursive: true });

  for (const roleName of Object.keys(roles)) {
    const agentName = agentNames[roleName] ?? roleName;
    const roleRecord = buildRoleRecord(alConfig, repoRoot, roleName);
    const modelSettings = resolveRoleModel(alConfig, 'copilot', roleName, copilotAdapter);
    const md = renderCopilotAgentMarkdown(
      agentName,
      roleName,
      roleRecord,
      modelSettings,
      absoluteSkillReferenceMap,
      agentNames
    );
    const mdPath = resolveCopilotAgentPath(outputDir, agentName);
    mkdirSync(dirname(mdPath), { recursive: true });
    writeFileSync(mdPath, md, 'utf-8');
    files.push(relative(outputDir, mdPath).replace(/\\/g, '/'));
  }

  files.push(...writeCopilotPublicSkill(
    outputDir,
    skillEntries,
    relativeSkillReferenceMap,
    agentNames,
    backendEntries
  ));

  const promptPath = resolveCopilotPromptPath(outputDir);
  mkdirSync(dirname(promptPath), { recursive: true });
  writeFileSync(
    promptPath,
    renderCopilotPromptMarkdown(agentNames.orchestrator ?? 'orchestrator', agentNames),
    'utf-8'
  );
  files.push(relative(outputDir, promptPath).replace(/\\/g, '/'));

  return { files };
}

/**
 * Plan Copilot adapter artifacts without writing to the filesystem.
 *
 * @param {object} alConfig
 * @param {string} repoRoot
 * @param {string} outputDir
 * @returns {{ actions: Array, files: string[], adapter: string }}
 */
export function planCopilotArtifacts(alConfig, repoRoot, outputDir) {
  const copilotAdapter = alConfig.adapters?.copilot ?? {};
  const roles = alConfig.roles ?? {};
  const roleBindings = copilotAdapter.roleBindings ?? {};
  const agentNames = Object.fromEntries(
    Object.keys(roles).map(roleName => [roleName, roleBindings[roleName]?.agent ?? roleName])
  );
  const skillEntries = readCanonicalSkillEntries(repoRoot, alConfig);
  const backendEntries = readCanonicalBackendEntries(repoRoot, alConfig);
  const relativeSkillReferenceMap = buildSkillReferenceMap(
    skillEntries, copilotSkillReferenceRelativePath
  );
  addBackendReferencesToMap(relativeSkillReferenceMap, backendEntries, copilotBackendReferenceRelativePath);
  const absoluteSkillReferenceMap = buildSkillReferenceMap(
    skillEntries, copilotSkillReferenceAbsolutePath
  );
  addBackendReferencesToMap(absoluteSkillReferenceMap, backendEntries, copilotBackendReferenceAbsolutePath);

  const actions = [];
  const files = [];

  actions.push({ type: 'clear-owned-directory', adapter: 'copilot', relPath: '.github/agents' });
  actions.push({ type: 'clear-owned-directory', adapter: 'copilot', relPath: '.github/prompts' });

  // .github/agents/<name>.agent.md
  for (const roleName of Object.keys(roles)) {
    const agentName = agentNames[roleName] ?? roleName;
    const roleRecord = buildRoleRecord(alConfig, repoRoot, roleName);
    const modelSettings = resolveRoleModel(alConfig, 'copilot', roleName, copilotAdapter);
    const md = renderCopilotAgentMarkdown(
      agentName, roleName, roleRecord, modelSettings, absoluteSkillReferenceMap, agentNames
    );
    const relPath = `.github/agents/${agentName}.agent.md`;
    actions.push({
      type: 'write-file',
      adapter: 'copilot',
      relPath,
      content: md,
      marker: COPILOT_GENERATED_MARKER,
    });
    files.push(relPath);
  }

  // Public skill directory.
  const skillDirRelPath = `.github/skills/${COPILOT_PUBLIC_SKILL_NAME}`;
  actions.push({
    type: 'clear-owned-directory',
    adapter: 'copilot',
    relPath: skillDirRelPath,
  });

  // SKILL.md
  actions.push({
    type: 'write-file',
    adapter: 'copilot',
    relPath: `${skillDirRelPath}/SKILL.md`,
    content: renderCopilotPublicSkill(relativeSkillReferenceMap, agentNames, backendEntries),
    marker: COPILOT_GENERATED_MARKER,
  });
  files.push(`${skillDirRelPath}/SKILL.md`);

  // Skill reference trees.
  for (const entry of skillEntries) {
    const refActions = planReferenceTree(
      entry.sourceDir,
      `${skillDirRelPath}/references/skills/${entry.canonicalName}`,
      'copilot',
      (content) => renderReferenceMarkdown(content, relativeSkillReferenceMap),
      COPILOT_GENERATED_MARKER
    );
    actions.push(...refActions);
    files.push(...refActions.map(a => a.relPath));
  }

  // Backend references.
  for (const entry of backendEntries) {
    const relPath = `${skillDirRelPath}/references/backends/${entry.filename}`;
    const content = renderCopilotGeneratedText(
      readFileSync(entry.sourceFile, 'utf-8'), relativeSkillReferenceMap
    );
    actions.push({ type: 'write-file', adapter: 'copilot', relPath, content });
    files.push(relPath);
  }

  // Prompt file.
  actions.push({
    type: 'write-file',
    adapter: 'copilot',
    relPath: COPILOT_PROMPT_RELATIVE_PATH,
    content: renderCopilotPromptMarkdown(agentNames.orchestrator ?? 'orchestrator', agentNames),
    marker: COPILOT_GENERATED_MARKER,
  });
  files.push(COPILOT_PROMPT_RELATIVE_PATH);

  return { actions, files, adapter: 'copilot' };
}
