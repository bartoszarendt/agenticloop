/**
 * GitHub Copilot host adapter.
 *
 * Status: supported.
 *
 * Repo-local Copilot MVP support focuses on explicit slash activation plus one
 * public Agentic Loop skill surface:
 *   - Custom agents under .github/agents/*.agent.md.
 *   - One public .github/skills/agenticloop/SKILL.md skill.
 *   - Internal procedure references under
 *     .github/skills/agenticloop/references/.
 *   - One IDE prompt-file fallback under .github/prompts/agenticloop.prompt.md.
 *
 * This MVP intentionally does NOT generate .github/copilot-instructions.md.
 * Repository-wide and path-specific Copilot instructions remain user or
 * team-owned surfaces.
 */

