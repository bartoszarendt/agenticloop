/**
 * Adapter output collision preflight.
 *
 * Before any adapter generator writes to the filesystem, this module
 * classifies each destination path to decide whether writing is safe.
 *
 * Classification:
 *   absent               — path does not exist; safe to create
 *   exact-owned          — path exists and matches the ownership manifest exactly; safe to update
 *   owned-modified       — path exists and is in manifest but has been modified; refuse by default
 *   unowned              — path exists, not in manifest, but in a generated host directory; refuse
 *   unrecognized         — path exists and is not in any known generated surface; refuse
 *   malformed-shared     — shared config file exists but cannot be parsed; preserve byte-for-byte
 */

import { existsSync, readFileSync, statSync } from 'node:fs';
import { join, normalize } from 'node:path';
import {
  classifyDirectory,
  classifyFile,
  loadManifest,
} from './generated-artifacts.js';

// ---------------------------------------------------------------------------
// Path normalization
// ---------------------------------------------------------------------------

function norm(relPath) {
  return normalize(relPath).replace(/\\/g, '/');
}

// ---------------------------------------------------------------------------
// Known generated host directories per adapter
// ---------------------------------------------------------------------------

const ADAPTER_HOST_DIRS = {
  opencode: ['.opencode'],
  codex: ['.codex', '.agents'],
  'claude-code': ['.claude'],
  copilot: ['.github'],
  cursor: ['.cursor'],
};

// Shared config file patterns that need special handling.
const SHARED_CONFIG_PATTERNS = [
  '.claude/settings.json',
  '.claude/settings.local.json',
  '.agents/plugins/marketplace.json',
  '.gitignore',
];

// Shared config files where malformed content must be preserved byte-for-byte.
const MALFORMABLE_SHARED_CONFIGS = new Set([
  '.agents/plugins/marketplace.json',
]);

// Files where Agentic Loop merges rather than replaces.
const MERGEABLE_SHARED_CONFIGS = new Set([
  '.claude/settings.json',
  '.claude/settings.local.json',
  '.agents/plugins/marketplace.json',
  '.gitignore',
]);

// Plugin distribution directories.
const PLUGIN_DIRS = new Set([
  'plugins/agenticloop',
]);

// ---------------------------------------------------------------------------
// Classification
// ---------------------------------------------------------------------------

/**
 * @typedef {Object} PathClassification
 * @property {'absent'|'exact-owned'|'owned-modified'|'unowned'|'unrecognized'|'malformed-shared'|'mergeable-shared'} status
 * @property {string} relPath
 * @property {string} adapter
 * @property {string} [message]
 * @property {boolean} safeToWrite  Whether the generator can proceed
 */

/**
 * Classify a single destination path for an adapter.
 *
 * @param {string} targetRoot
 * @param {string} relPath     Repo-relative destination path (forward slashes)
 * @param {string} adapter     Adapter name
 * @returns {PathClassification}
 */
export function classifyDestination(targetRoot, relPath, adapter) {
  const normalized = norm(relPath);
  const fullPath = join(targetRoot, normalized);

  // 1. Path does not exist — safe to create.
  if (!existsSync(fullPath)) {
    return { status: 'absent', relPath: normalized, adapter, safeToWrite: true };
  }

  // 2. Check the ownership manifest.
  const manifest = loadManifest(targetRoot);
  if (manifest) {
    const fileClassification = statSync(fullPath).isDirectory()
      ? classifyDirectory(targetRoot, normalized)
      : classifyFile(targetRoot, normalized);

    if (fileClassification.status === 'exact-owned') {
      return {
        status: 'exact-owned',
        relPath: normalized,
        adapter,
        safeToWrite: true,
        message: 'Previously generated by Agentic Loop; safe to update',
      };
    }

    if (fileClassification.status === 'owned-modified') {
      return {
        status: 'owned-modified',
        relPath: normalized,
        adapter,
        safeToWrite: false,
        message: 'Previously generated by Agentic Loop but has been modified; refusing to overwrite',
      };
    }
  }

  // 3. Mergeable shared config — Agentic Loop merges rather than replaces.
  if (MERGEABLE_SHARED_CONFIGS.has(normalized)) {
    // Check for malformed content.
    if (MALFORMABLE_SHARED_CONFIGS.has(normalized) && normalized.endsWith('.json')) {
      try {
        const text = readFileSync(fullPath, 'utf-8');
        JSON.parse(text);
      } catch {
        return {
          status: 'malformed-shared',
          relPath: normalized,
          adapter,
          safeToWrite: false,
          message: 'Shared config has malformed JSON; refusing to overwrite',
        };
      }
    }
    return {
      status: 'mergeable-shared',
      relPath: normalized,
      adapter,
      safeToWrite: true,
      message: 'Shared config; will be merged',
    };
  }

  // 4. Plugin directory.
  if (PLUGIN_DIRS.has(normalized) || PLUGIN_DIRS.has(normalized.split('/')[0] + '/' + normalized.split('/')[1])) {
    return {
      status: 'unowned',
      relPath: normalized,
      adapter,
      safeToWrite: false,
      message: 'Plugin directory may contain user content; refusing to overwrite',
    };
  }

  // 5. Check if the path is inside a known generated host directory for this adapter.
  const hostDirs = ADAPTER_HOST_DIRS[adapter] ?? [];
  const isInsideHostDir = hostDirs.some(dir => normalized.startsWith(dir + '/'));
  if (isInsideHostDir) {
    // Inside a generated host directory but not in the manifest — likely
    // a user file in a generated directory. Refuse to overwrite.
    return {
      status: 'unowned',
      relPath: normalized,
      adapter,
      safeToWrite: false,
      message: 'File in generated host directory but not tracked by Agentic Loop; refusing to overwrite',
    };
  }

  // 6. Completely unrecognized — outside all known generated surfaces.
  return {
    status: 'unrecognized',
    relPath: normalized,
    adapter,
    safeToWrite: false,
    message: 'File is not in any known Agentic Loop generated surface; refusing to overwrite',
  };
}

// ---------------------------------------------------------------------------
// Batch preflight
// ---------------------------------------------------------------------------

/**
 * @typedef {Object} PreflightResult
 * @property {PathClassification[]} classifications
 * @property {PathClassification[]} blocked  Paths that would be refused
 * @property {boolean} allClear  True if no paths are blocked
 */

/**
 * Run collision preflight for a set of planned destination paths.
 *
 * @param {string} targetRoot
 * @param {string} adapter
 * @param {string[]} plannedPaths  Repo-relative paths the generator plans to write
 * @returns {PreflightResult}
 */
export function preflightGeneration(targetRoot, adapter, plannedPaths) {
  const seen = new Set();
  const classifications = [];
  const blocked = [];

  for (const relPath of plannedPaths) {
    const normalized = norm(relPath);
    if (seen.has(normalized)) continue;
    seen.add(normalized);

    const cls = classifyDestination(targetRoot, normalized, adapter);
    classifications.push(cls);
    if (!cls.safeToWrite) {
      blocked.push(cls);
    }
  }

  return {
    classifications,
    blocked,
    allClear: blocked.length === 0,
  };
}

/**
 * Format a preflight result for human-readable output.
 *
 * @param {PreflightResult} result
 * @returns {string[]}
 */
export function formatPreflightResult(result) {
  const lines = [];
  for (const cls of result.classifications) {
    if (cls.status === 'absent') continue; // No need to report creates
    if (cls.safeToWrite) {
      lines.push(`  ${cls.relPath}: ${cls.message ?? cls.status}`);
    } else {
      lines.push(`  BLOCKED ${cls.relPath}: ${cls.message ?? cls.status}`);
    }
  }
  return lines;
}
