describe('parseModelMutations', () => {
  it('parses role/model/reasoning-effort triples', () => {
    const args = [
      '--role', 'orchestrator',
      '--model', 'provider/model-a',
      '--reasoning-effort', 'high',
      '--role', 'engineer',
      '--model', 'provider/model-b',
    ];
    const mutations = parseModelMutations(args);
    assert.deepEqual(mutations, [
      { role: 'orchestrator', model: 'provider/model-a', reasoningEffort: 'high' },
      { role: 'engineer', model: 'provider/model-b' },
    ]);
  });

  it('returns empty array when no options are given', () => {
    assert.deepEqual(parseModelMutations([]), []);
  });
});

