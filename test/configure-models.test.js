describe('configureModels', () => {
  it('writes adapter roleSettings to agenticloop.json', () => {
    const d = makeTarget();
    const result = configureModels(d, {
      adapter: 'opencode',
      mutations: [
        { role: 'orchestrator', model: 'openrouter/deepseek/deepseek-v4', reasoningEffort: 'high' },
        { role: 'engineer', model: 'openrouter/xiaomi/mimo', reasoningEffort: 'high' },
      ],
    });
    assert.deepEqual(result.errors, []);
    assert.ok(result.updated.includes('adapters.opencode.roleSettings.orchestrator.model'));


    const cfg = loadJsonFile(join(d, 'agenticloop.json'));
    assert.equal(cfg.adapters?.opencode?.roleSettings?.orchestrator?.model, 'openrouter/deepseek/deepseek-v4');
    assert.equal(cfg.adapters?.opencode?.roleSettings?.orchestrator?.reasoningEffort, 'high');
    assert.equal(cfg.adapters?.opencode?.roleSettings?.engineer?.model, 'openrouter/xiaomi/mimo');
  });

  it('configures a non-interactive --role auditor model and preserves other roles', () => {
    const d = makeTarget();
    // Seed an explicit orchestrator model to prove it is preserved.
    configureModels(d, { adapter: 'opencode', mutations: [{ role: 'orchestrator', model: 'keep/me' }] });

    const result = configureModels(d, {
      adapter: 'opencode',
      mutations: parseModelMutations(['--role', 'auditor', '--model', 'openrouter/best-auditor', '--reasoning-effort', 'high']),
    });
    assert.deepEqual(result.errors, []);
    assert.ok(result.updated.includes('adapters.opencode.roleSettings.auditor.model'));

    const cfg = loadJsonFile(join(d, 'agenticloop.json'));
    assert.equal(cfg.adapters.opencode.roleSettings.auditor.model, 'openrouter/best-auditor');
    assert.equal(cfg.adapters.opencode.roleSettings.auditor.reasoningEffort, 'high');
    assert.equal(cfg.adapters.opencode.roleSettings.orchestrator.model, 'keep/me');
  });

  it('errors when adapter is missing', () => {
    const d = makeTarget();
    const result = configureModels(d, { mutations: [{ role: 'orchestrator', model: 'x' }] });
    assert.ok(result.errors.some(e => e.includes('--adapter')));
  });

  it('errors when agenticloop.json is missing', () => {
    const d = mkdtempSync(join(tmpDir, 'no-config-'));
    const result = configureModels(d, { adapter: 'opencode', mutations: [{ role: 'orchestrator', model: 'x' }] });
    assert.ok(result.errors.some(e => e.includes('agenticloop.json not found')));
  });

  it('errors for unknown adapter', () => {
    const d = makeTarget();
    const result = configureModels(d, { adapter: 'unknown', mutations: [{ role: 'orchestrator', model: 'x' }] });
    assert.ok(result.errors.some(e => e.includes('Unknown adapter')));
  });

  it('rewrites agenticloop.json as formatted strict JSON', () => {
    const d = makeTarget();
    const cfgPath = join(d, 'agenticloop.json');
    writeFileSync(cfgPath, '{"extends":"./agenticloop/config.json","adapters":{"opencode":{"roleSettings":{}}}}\n', 'utf-8');

    const result = configureModels(d, {
      adapter: 'opencode',
      mutations: [{ role: 'engineer', model: 'configured/model', reasoningEffort: 'xhigh' }],
    });

    assert.deepEqual(result.errors, []);
    assert.deepEqual(result.warnings, []);
    const text = readFileSync(cfgPath, 'utf-8');
    assert.match(text, /^\{\n  "extends": "\.\/agenticloop\/config\.json",/);
    const cfg = loadJsonFile(cfgPath);
    assert.equal(cfg.adapters.opencode.roleSettings.engineer.model, 'configured/model');
    assert.equal(cfg.adapters.opencode.roleSettings.engineer.reasoningEffort, 'xhigh');
  });

  it('ignores reasoningEffort for Claude Code role settings', () => {
    const d = makeTarget();
    const result = configureModels(d, {
      adapter: 'claude-code',
      mutations: [
        { role: 'engineer', model: 'opus', reasoningEffort: 'max' },
      ],
    });

    assert.deepEqual(result.errors, []);
    assert.ok(result.warnings.some(w => w.includes('Skipping reasoningEffort for claude-code')));
    assert.ok(result.updated.includes('adapters.claude-code.roleSettings.engineer.model'));
    assert.ok(!result.updated.includes('adapters.claude-code.roleSettings.engineer.reasoningEffort'));

    const cfg = loadJsonFile(join(d, 'agenticloop.json'));
    assert.equal(cfg.adapters['claude-code'].roleSettings.engineer.model, 'opus');
    assert.equal(cfg.adapters['claude-code'].roleSettings.engineer.reasoningEffort, undefined);
  });

  it('ignores reasoningEffort for Copilot role settings', () => {
    const d = makeTarget();
    const result = configureModels(d, {
      adapter: 'copilot',
      mutations: [
        { role: 'engineer', model: 'gpt-5.4', reasoningEffort: 'high' },
      ],
    });

    assert.deepEqual(result.errors, []);
    assert.ok(result.warnings.some(w => w.includes('Skipping reasoningEffort for copilot')));
    assert.ok(result.updated.includes('adapters.copilot.roleSettings.engineer.model'));
    assert.ok(!result.updated.includes('adapters.copilot.roleSettings.engineer.reasoningEffort'));

    const cfg = loadJsonFile(join(d, 'agenticloop.json'));
    assert.equal(cfg.adapters.copilot.roleSettings.engineer.model, 'gpt-5.4');
    assert.equal(cfg.adapters.copilot.roleSettings.engineer.reasoningEffort, undefined);
  });

  it('ignores reasoningEffort for Cursor role settings', () => {
    const d = makeTarget();
    const result = configureModels(d, {
      adapter: 'cursor',
      mutations: [
        { role: 'engineer', model: 'gpt-5.5', reasoningEffort: 'high' },
      ],
    });

    assert.deepEqual(result.errors, []);
    assert.ok(result.warnings.some(w => w.includes('Skipping reasoningEffort for cursor')));
    assert.ok(result.updated.includes('adapters.cursor.roleSettings.engineer.model'));
    assert.ok(!result.updated.includes('adapters.cursor.roleSettings.engineer.reasoningEffort'));

    const cfg = loadJsonFile(join(d, 'agenticloop.json'));
    assert.equal(cfg.adapters.cursor.roleSettings.engineer.model, 'gpt-5.5');
    assert.equal(cfg.adapters.cursor.roleSettings.engineer.reasoningEffort, undefined);
  });

  it('applies the recommended Codex profile without replacing explicit fields', () => {
    const d = makeTarget();
    writeFileSync(join(d, 'agenticloop.json'), JSON.stringify({
      extends: './agenticloop/config.json',
      adapters: {
        codex: {
          roleSettings: {
            orchestrator: { model: 'custom-orchestrator' },
            maintainer: { reasoningEffort: 'minimal' },
          },
        },
      },
    }, null, 2) + '\n');

    const result = configureModels(d, { adapter: 'codex', profile: 'recommended' });

    assert.deepEqual(result.errors, []);
    assert.ok(result.updated.includes('adapters.codex.roleSettings.orchestrator.reasoningEffort'));
    assert.ok(result.preserved.includes('adapters.codex.roleSettings.orchestrator.model'));
    assert.ok(result.preserved.includes('adapters.codex.roleSettings.maintainer.reasoningEffort'));
    const cfg = loadJsonFile(join(d, 'agenticloop.json'));
    assert.equal(cfg.adapters.codex.roleSettings.orchestrator.model, 'custom-orchestrator');
    assert.equal(cfg.adapters.codex.roleSettings.orchestrator.reasoningEffort, 'xhigh');
    assert.equal(cfg.adapters.codex.roleSettings.maintainer.model, 'gpt-5.6-terra');
    assert.equal(cfg.adapters.codex.roleSettings.maintainer.reasoningEffort, 'minimal');
    assert.deepEqual(cfg.adapters.codex.roleSettings.engineer, {
      model: 'gpt-5.6-terra',
      reasoningEffort: 'high',
    });
  });

  it('keeps default-profile objects isolated between callers', () => {
    const first = getDefaultRoleSettings('codex');
    first.engineer.model = 'changed';
    assert.equal(getDefaultRoleSettings('codex').engineer.model, 'gpt-5.6-terra');
    assert.deepEqual(getDefaultRoleSettings('unknown'), {});
  });

  it('reports added and preserved fields for the Codex recommended CLI profile', () => {
    const d = makeTarget();
    writeFileSync(join(d, 'agenticloop.json'), JSON.stringify({
      extends: './agenticloop/config.json',
      adapters: { codex: { roleSettings: { orchestrator: { model: 'custom' } } } },
    }, null, 2) + '\n');

    const result = spawnSync(process.execPath, [
      BIN, 'configure', 'models', '--target', d, '--adapter', 'codex', '--profile', 'recommended',
    ], { encoding: 'utf-8' });

    assert.equal(result.status, 0, result.stderr);
    assert.match(result.stdout, /added: adapters\.codex\.roleSettings\.orchestrator\.reasoningEffort/);
    assert.match(result.stdout, /kept: adapters\.codex\.roleSettings\.orchestrator\.model/);
    assert.match(result.stdout, /agenticloop generate codex/);
  });

  it('rejects a recommended profile mixed with explicit model mutations', () => {
    const d = makeTarget();
    const result = spawnSync(process.execPath, [
      BIN, 'configure', 'models', '--target', d, '--adapter', 'codex', '--profile', 'recommended',
      '--role', 'engineer', '--model', 'other',
    ], { encoding: 'utf-8' });

    assert.notEqual(result.status, 0);
    assert.match(result.stderr, /cannot be combined/);
  });

  it('rejects the recommended profile for a non-Codex adapter', () => {
    const d = makeTarget();
    const result = spawnSync(process.execPath, [
      BIN, 'configure', 'models', '--target', d, '--adapter', 'opencode', '--profile', 'recommended',
    ], { encoding: 'utf-8' });

    assert.notEqual(result.status, 0);
    assert.match(result.stderr, /available only for --adapter codex/);
  });
});

