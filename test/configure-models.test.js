describe('createPrompts', () => {
  it('close() removes listeners and pauses the input stream', () => {
    const input = new PassThrough();
    let paused = false;
    const originalPause = input.pause.bind(input);
    input.pause = () => { paused = true; return originalPause(); };

    const prompts = createPrompts(input, { write: () => {} });

    assert.equal(input.listenerCount('data'), 1);
    assert.equal(input.listenerCount('end'), 1);

    prompts.close();

    assert.equal(input.listenerCount('data'), 0);
    assert.equal(input.listenerCount('end'), 0);
    assert.ok(paused, 'close() must pause the input stream');
  });

  it('close() tolerates streams without pause()', () => {
    const input = new PassThrough();
    input.pause = undefined;

    const prompts = createPrompts(input, { write: () => {} });
    prompts.close();

    assert.equal(input.listenerCount('data'), 0);
    assert.equal(input.listenerCount('end'), 0);
  });
});

describe('promptModelSettings', () => {
  it('resolves a pending prompt when stdin ends without a trailing newline', async () => {
    const input = new PassThrough();
    let output = '';
    const prompts = createPrompts(input, {
      write: (text) => { output += text; },
    });

    const answer = prompts.ask('model: ');
    input.end('provider/model');

    assert.equal(await answer, 'provider/model');
    assert.equal(output, 'model: ');
    prompts.close();
  });

  it('collects model and reasoning effort per role', async () => {
    const responses = ['model-a', 'high', '', 'model-b', ''];
    let i = 0;
    const prompts = {
      ask: async () => responses[i++],
    };
    const mutations = await promptModelSettings(['orchestrator', 'maintainer', 'engineer'], 'opencode', prompts);
    assert.deepEqual(mutations, [
      { role: 'orchestrator', model: 'model-a', reasoningEffort: 'high' },
      { role: 'engineer', model: 'model-b' },
    ]);
  });

  it('does not prompt for reasoning effort for Claude Code', async () => {
    const responses = ['sonnet', 'opus'];
    const questions = [];
    let i = 0;
    const prompts = {
      ask: async (question) => {
        questions.push(question);
        return responses[i++];
      },
    };

    const mutations = await promptModelSettings(['orchestrator', 'engineer'], 'claude-code', prompts);

    assert.deepEqual(mutations, [
      { role: 'orchestrator', model: 'sonnet' },
      { role: 'engineer', model: 'opus' },
    ]);
    assert.ok(questions.every(q => !q.includes('reasoning effort')));
  });

  it('does not prompt for reasoning effort for Copilot', async () => {
    const responses = ['gpt-5.4', 'gpt-5.5'];
    const questions = [];
    let i = 0;
    const prompts = {
      ask: async (question) => {
        questions.push(question);
        return responses[i++];
      },
    };

    const mutations = await promptModelSettings(['orchestrator', 'engineer'], 'copilot', prompts);

    assert.deepEqual(mutations, [
      { role: 'orchestrator', model: 'gpt-5.4' },
      { role: 'engineer', model: 'gpt-5.5' },
    ]);
    assert.ok(questions.every(q => !q.includes('reasoning effort')));
  });

  it('does not prompt for reasoning effort for Cursor', async () => {
    const responses = ['gpt-5.5', 'gpt-5.5-mini'];
    const questions = [];
    let i = 0;
    const prompts = {
      ask: async (question) => {
        questions.push(question);
        return responses[i++];
      },
    };

    const mutations = await promptModelSettings(['orchestrator', 'engineer'], 'cursor', prompts);

    assert.deepEqual(mutations, [
      { role: 'orchestrator', model: 'gpt-5.5' },
      { role: 'engineer', model: 'gpt-5.5-mini' },
    ]);
    assert.ok(questions.every(q => !q.includes('reasoning effort')));
  });

  it('prompts Codex with the supported reasoning effort values', async () => {
    const questions = [];
    const prompts = {
      ask: async (question) => {
        questions.push(question);
        return questions.length === 1 ? 'gpt-5.4' : 'xhigh';
      },
    };

    const mutations = await promptModelSettings(['engineer'], 'codex', prompts);

    assert.deepEqual(mutations, [
      { role: 'engineer', model: 'gpt-5.4', reasoningEffort: 'xhigh' },
    ]);
    assert.ok(questions.some(q => q.includes('minimal, low, medium, high, xhigh')));
  });

  it('returns empty array when all roles are skipped', async () => {
    const prompts = {
      ask: async () => '',
    };
    const mutations = await promptModelSettings(['orchestrator', 'maintainer'], 'opencode', prompts);
    assert.deepEqual(mutations, []);
  });
});

