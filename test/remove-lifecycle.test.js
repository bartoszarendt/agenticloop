describe('remove - Claude settings reversal', () => {
  it('removes settings file when only Agentic Loop entries exist', () => {
    const t = makeTarget();
    mkdirSync(join(t, '.claude'), { recursive: true });
    writeFileSync(join(t, '.claude', 'settings.json'), JSON.stringify({
      permissions: {
        allow: ['Bash(npx agenticloop *)', 'PowerShell(npx agenticloop *)'],
      },
    }, null, 2) + '\n');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.removed.some(r => r.includes('settings.json')));
    assert.ok(!existsSync(join(t, '.claude', 'settings.json')));
  });

  it('removes only Agentic Loop entries from mixed settings', () => {
    const t = makeTarget();
    mkdirSync(join(t, '.claude'), { recursive: true });
    writeFileSync(join(t, '.claude', 'settings.json'), JSON.stringify({
      permissions: {
        allow: [
          'Bash(git status *)',
          'Bash(npx agenticloop *)',
          'PowerShell(npx agenticloop *)',
        ],
      },
    }, null, 2) + '\n');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.removed.some(r => r.includes('permission entries removed')));

    const remaining = JSON.parse(readFileSync(join(t, '.claude', 'settings.json'), 'utf-8'));
    assert.deepEqual(remaining.permissions.allow, ['Bash(git status *)']);
  });

  it('preserves settings with no Agentic Loop entries', () => {
    const t = makeTarget();
    mkdirSync(join(t, '.claude'), { recursive: true });
    writeFileSync(join(t, '.claude', 'settings.json'), JSON.stringify({
      permissions: {
        allow: ['Bash(git status *)'],
      },
    }, null, 2) + '\n');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.skipped.some(s => s.includes('no Agentic Loop permission entries found')));
    assert.ok(existsSync(join(t, '.claude', 'settings.json')));
  });
});

