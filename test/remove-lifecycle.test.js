/**
 * Lifecycle tests for src/remove.js — safe reversible removal.
 *
 * Covers:
 *   - Marker-based removal of generated artifacts
 *   - Claude agents with custom roleBindings filenames
 *   - Codex legacy skills require strong marker (not just name)
 *   - Claude settings reversal
 *   - Public skill directory preservation
 *   - Dry-run accuracy
 *   - Malformed marketplace JSON preservation
 */

import { describe, it, before, after } from 'node:test';
import assert from 'node:assert/strict';
import {
  existsSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  readdirSync,
  rmSync,
  writeFileSync,
} from 'node:fs';
import { join } from 'node:path';
import { tmpdir } from 'node:os';

import { removeAgenticLoop } from '../src/remove.js';

let tmpBase;

before(() => {
  tmpBase = mkdtempSync(join(tmpdir(), 'al-remove-test-'));
});

after(() => {
  rmSync(tmpBase, { recursive: true, force: true });
});

function makeTarget() {
  return mkdtempSync(join(tmpBase, 'target-'));
}

// ---------------------------------------------------------------------------
// Claude agents with custom filenames
// ---------------------------------------------------------------------------

describe('remove - Claude agents with custom roleBindings', () => {
  it('removes generated Claude agents with custom filenames', () => {
    const t = makeTarget();
    mkdirSync(join(t, '.claude', 'agents'), { recursive: true });

    // Custom roleBindings: architect instead of orchestrator
    writeFileSync(join(t, '.claude', 'agents', 'architect.md'),
      '---\nname: "orchestrator"\n---\n\n<!-- Generated by: agenticloop generate claude-code -->\n\n# Architect');
    writeFileSync(join(t, '.claude', 'agents', 'coder.md'),
      '---\nname: "engineer"\n---\n\n<!-- Generated by: agenticloop generate claude-code -->\n\n# Coder');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.removed.includes('.claude/agents/architect.md'));
    assert.ok(result.removed.includes('.claude/agents/coder.md'));
    assert.ok(!existsSync(join(t, '.claude', 'agents', 'architect.md')));
    assert.ok(!existsSync(join(t, '.claude', 'agents', 'coder.md')));
  });

  it('preserves unmarked Claude agents', () => {
    const t = makeTarget();
    mkdirSync(join(t, '.claude', 'agents'), { recursive: true });

    writeFileSync(join(t, '.claude', 'agents', 'my-custom.md'), '# My Custom Agent');
    writeFileSync(join(t, '.claude', 'agents', 'orchestrator.md'),
      '<!-- Generated by: agenticloop generate claude-code -->\n# Orchestrator');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.removed.includes('.claude/agents/orchestrator.md'));
    assert.ok(result.skipped.some(s => s.includes('.claude/agents/my-custom.md')));
    assert.ok(existsSync(join(t, '.claude', 'agents', 'my-custom.md')));
  });
});

