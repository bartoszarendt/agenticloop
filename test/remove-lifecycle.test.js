describe('remove - Codex legacy skills require strong marker', () => {
  it('preserves a hand-authored skill named agenticloop-role-delegation', () => {
    const t = makeTarget();
    const skillDir = join(t, '.agents', 'skills', 'agenticloop-role-delegation');
    mkdirSync(skillDir, { recursive: true });
    // Hand-authored skill: no generated marker, no generated structure
    writeFileSync(join(skillDir, 'SKILL.md'), '# My Role Delegation Skill\nThis is hand-authored.');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.skipped.some(s => s.includes('agenticloop-role-delegation')));
    assert.ok(existsSync(join(skillDir, 'SKILL.md')));
  });

  it('removes a legacy skill with strong marker', () => {
    const t = makeTarget();
    const skillDir = join(t, '.agents', 'skills', 'agenticloop-role-delegation');
    mkdirSync(skillDir, { recursive: true });
    writeFileSync(join(skillDir, 'SKILL.md'),
      '# Skill\n<!-- Generated by: agenticloop generate codex. Do not edit by hand. -->');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.removed.includes('.agents/skills/agenticloop-role-delegation'));
    assert.ok(!existsSync(skillDir));
  });

  it('removes a legacy skill with exact generated structure', () => {
    const t = makeTarget();
    const skillDir = join(t, '.agents', 'skills', 'agenticloop-role-delegation');
    mkdirSync(join(skillDir, 'agents'), { recursive: true });
    mkdirSync(join(skillDir, 'references', 'skills'), { recursive: true });
    writeFileSync(join(skillDir, 'SKILL.md'), '# Skill\nSome content');
    writeFileSync(join(skillDir, 'agents', 'openai.yaml'), 'name: test');

    const result = removeAgenticLoop({ target: t, dryRun: false });
    assert.ok(result.removed.includes('.agents/skills/agenticloop-role-delegation'));
  });
});

