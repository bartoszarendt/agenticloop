// Dry-run accuracy
// ---------------------------------------------------------------------------

describe('remove - dry-run', () => {
  it('dry-run reports same paths as real removal without deleting files', () => {
    const t1 = makeTarget();
    const t2 = makeTarget();

    for (const t of [t1, t2]) {
      mkdirSync(join(t, '.claude', 'agents'), { recursive: true });
      writeFileSync(join(t, '.claude', 'agents', 'orchestrator.md'),
        '<!-- Generated by: agenticloop generate claude-code -->\n# Orch');
      mkdirSync(join(t, '.opencode', 'agents'), { recursive: true });
      writeFileSync(join(t, '.opencode', 'agents', 'orchestrator.md'),
        '<!-- Generated by Agentic Loop. Do not edit by hand. Source: agenticloop.json (adapters.opencode.roleSettings). Refresh: agenticloop update --adapter opencode -->\n# Orch');
    }

    const dryResult = removeAgenticLoop({ target: t1, dryRun: true });
    const realResult = removeAgenticLoop({ target: t2, dryRun: false });

    // Dry-run reports the same file-level paths as real removal
    // (directory cleanup differs because dry-run doesn't empty directories)
    const isFileRemoved = p => {
      const lastSegment = p.split(/[\\/]/).pop() ?? '';
      // Files have an extension like .md, .toml, .json; directories like .claude don't
      return /\w+\.\w+$/.test(lastSegment);
    };
    const dryFiles = dryResult.removed.filter(isFileRemoved);
    const realFiles = realResult.removed.filter(isFileRemoved);
    assert.deepEqual(dryFiles.sort(), realFiles.sort());

    // Dry-run does NOT delete files
    assert.ok(existsSync(join(t1, '.claude', 'agents', 'orchestrator.md')));
    assert.ok(existsSync(join(t1, '.opencode', 'agents', 'orchestrator.md')));

    // Real removal DOES delete files
    assert.ok(!existsSync(join(t2, '.claude', 'agents', 'orchestrator.md')));
    assert.ok(!existsSync(join(t2, '.opencode', 'agents', 'orchestrator.md')));
  });
});

